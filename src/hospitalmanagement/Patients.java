    private void PatCount(){
        try{
            Stl = Con.createStatement();
            Rsl = Stl.executeQuery("select Max(PATID) from hospital.PatientTbl");
            Rsl.next();
            PatId = Rsl.getInt(1)+1;
        } catch (Exception Ex){
            Ex.printStackTrace();
        }
    }
    
