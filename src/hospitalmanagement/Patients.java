    private void DisplayPatients(){
        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/HospitalDatabase","hospital","admin");
            St = Con.createStatement();
            Rs = St.executeQuery("Select  PATID as ID, PATNAME as NAME, PHONE, PATADDRESS as ADDRESS, DOB as BIRTHDATE, PATGENDER as GENDER, PATALLERGIES as ALLERGIES from hospital.PatientTbl");
            PatTable.setModel(DbUtils.resultSetToTableModel(Rs));
        } catch (Exception Ex){
            Ex.printStackTrace();
        }
    }
